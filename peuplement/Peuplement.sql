CREATE TABLE address (
addressid int PRIMARY KEY, 
street varchar(40) NOT NULL, 
region varchar(40) NOT NULL, 
postalcode varchar(40) NOT NULL, 
city varchar(40) NOT NULL
);

CREATE TABLE `client` (
clientid int PRIMARY KEY, 
clientname varchar(40), 
surname varchar(40), 
companyname varchar(40), 
mail varchar(40) NOT NULL, 
phone varchar(40) NOT NULL, 
contact varchar(40), 
addressid  int NOT NULL,
FOREIGN KEY (addressid) REFERENCES address (addressid)
);


CREATE TABLE purchase(
purchasenum int PRIMARY KEY, 
deliverydate datetime NOT NULL, 
orderdate datetime NOT NULL, 
purchasecost int NOT NULL,
clientid int NOT NULL , 
FOREIGN KEY (clientid) REFERENCES client (clientid),
addressid int NOT NULL,
FOREIGN KEY (addressid) REFERENCES address (addressid)
);

CREATE TABLE part(
partnum varchar(20) PRIMARY KEY, 
partdesc varchar(40) NOT NULL, 
stockpartnumber int NOT NULL,
price int NOT NULL
);

CREATE TABLE supplier (
siret varchar(20) PRIMARY KEY, 
suppliername varchar(40) NOT NULL, 
contactname varchar(40) NOT NULL, 
label varchar(40), 
addressid int NOT NULL,
FOREIGN KEY (addressid) REFERENCES address (addressid)
);

CREATE TABLE bicycle (
bicycleid int PRIMARY KEY, 
bname varchar(40) NOT NULL, 
size varchar(40) NOT NULL, 
cost decimal NOT NULL, 
type varchar(40) NOT NULL, 
introddate datetime NOT NULL, 
deprecdate datetime NOT NULL, 
stockbicyclenumber int NOT NULL
);



CREATE TABLE loyaltyprogram (
programnum int PRIMARY KEY, 
fee int NOT NULL, 
length int NOT NULL, 
discount int NOT NULL, 
programdesc varchar(40) NOT NULL
);



CREATE TABLE providedpart (
partnum varchar(20) NOT NULL, 
FOREIGN KEY (partnum) REFERENCES part (partnum),
siret varchar(20) NOT NULL, 
FOREIGN KEY (siret) REFERENCES supplier (siret),
price decimal NOT NULL, 
introdate datetime NOT NULL, 
deprecateddate datetime NOT NULL, 
delay int NOT NULL,
PRIMARY KEY (partnum, siret)
);

CREATE TABLE orderedpart (
purchasenum int NOT NULL, 
FOREIGN KEY (purchasenum) REFERENCES purchase (purchasenum),
partnum varchar(20) NOT NULL, 
FOREIGN KEY (partnum) REFERENCES part (partnum),
orderedpartnb int NOT NULL,
PRIMARY KEY (purchasenum, partnum)
);

CREATE TABLE composition (
partnum varchar(20) NOT NULL, 
FOREIGN KEY (partnum) REFERENCES part (partnum),
bicycleid int NOT NULL,
FOREIGN KEY (bicycleid) REFERENCES bicycle (bicycleid),
PRIMARY KEY (partnum, bicycleid)
);

CREATE TABLE subscription (
clientid int NOT NULL, 
FOREIGN KEY (clientid) REFERENCES client (clientid),
programnum int NOT NULL, 
FOREIGN KEY (programnum) REFERENCES loyaltyprogram (programnum),
programstartdate datetime NOT NULL,
PRIMARY KEY (clientid, programnum)
);

CREATE TABLE orderedbicycle (
purchasenum int NOT NULL, 
FOREIGN KEY (purchasenum) REFERENCES purchase (purchasenum),
bicycleid int NOT NULL, 
FOREIGN KEY (bicycleid) REFERENCES bicycle (bicycleid),
orderedbicyclenb int NOT NULL,
PRIMARY KEY (purchasenum, bicycleid)
);


